import UIKit

// MARK: - Colors -

extension UIColor {
    
    /// Red color from the N64 logo.
    static var nintendoRed: UIColor { return UIColor(red: 255/255, green: 30/255, blue: 27/255, alpha: 1.0) }
    /// Green color from the N64 logo.
    static var nintendoGreen: UIColor { return UIColor(red: 0/255, green: 137/255, blue: 50/255, alpha: 1.0) }
    /// A faded neutral gray.
    static var nintendoFadedGray: UIColor { return UIColor(red: 0.5, green: 0.5, blue: 0.5, alpha: 0.6) }
}
